class Trip{
    constructor(coordinatesArray, departureCity, destinationCity, departure, destination, departureDate, returnDate, cabin, tt, departureSchedule, returnSchedule, adultAmount, childAmount, departureScheduleSelected, returnScheduleSelected){
        this.coordinatesArray = coordinatesArray;
        this.departureCity = departureCity;
        this.destinationCity = destinationCity;
        this.departure = departure;
        this.destination = destination;
        this.departureDate = departureDate;
        this.returnDate = returnDate;
        this.cabin = cabin;
        this.tt = tt;
        this.departureSchedule = departureSchedule;
        this.returnSchedule = returnSchedule;
        this.adultAmount = adultAmount;
        this.childAmount = childAmount;
        this.departureScheduleSelected = departureScheduleSelected;
        this.returnScheduleSelected = returnScheduleSelected;
    }
}